package lesson01;

public class FloatDoubleType {
	public static void main(String[] args) {
		
		float f = 3.141592653433f;
		double d = 3.141592653433d;
		
		System.out.println("float : " + f);
		System.out.println("double : " + d);
		
		System.out.println(3.14);
		System.out.println(1);
		System.out.println(3.14 + 1);
		
		
		
		
//		float f2 = 3.141592653433; 	//f는 생략 불가능
//		System.out.println("float2 : " + f2);
		
		double d2 = 3.141592653433; 	//d 생략가능
		System.out.println("double2 : " + d2);
	}

}
